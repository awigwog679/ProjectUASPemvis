    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
    String jk = null;
    if(rlaki.isSelected()){
        jk = "Laki-Laki";
    } else if(rperempuan.isSelected()){
        jk = "Perempuan";
    }

    String jenis = cbagama.getSelectedItem().toString();

    String sql = "INSERT INTO kasir (id_kasir,nama_kasir,jenis_kelamin,agama,alamat,no_telp,password) VALUES (?,?,?,?,?,?,?)";

    try{
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtid.getText());
        stat.setString(2, txtnm.getText());
        stat.setString(3, jk);
        stat.setString(4, jenis);
        stat.setString(5, txtalamat.getText());
        stat.setString(6, txttelp.getText());
        stat.setString(7, txtpassword.getText());

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "data berhasil disimpan");

        kosong();
        txtid.requestFocus();

    } catch(SQLException e){
        JOptionPane.showMessageDialog(null, "data gagal disimpan "+e);
    }

    datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        String jk = null;
    if(rlaki.isSelected()){
        jk = "Laki-Laki";
    } else if(rperempuan.isSelected()){
        jk = "Perempuan";
    }

    String agama = cbagama.getSelectedItem().toString();

    try{
        String sql = "UPDATE kasir SET nama_kasir=?, jenis_kelamin=?, agama=?, alamat=?, no_telp=?, password=? WHERE id_kasir=?";
        PreparedStatement stat = conn.prepareStatement(sql);

        stat.setString(1, txtnm.getText());
        stat.setString(2, jk);
        stat.setString(3, agama);
        stat.setString(4, txtalamat.getText());
        stat.setString(5, txttelp.getText());
        stat.setString(6, txtpassword.getText());
        stat.setString(7, txtid.getText());

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "data berhasil diubah");

        kosong();
        txtid.requestFocus();

    } catch(SQLException e){
        JOptionPane.showMessageDialog(null, "data gagal diubah "+e);
    }

    datatable();
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
    int ok = JOptionPane.showConfirmDialog(null,"hapus","konfirmasi dialog",JOptionPane.YES_NO_OPTION);
    if(ok==0){
        String sql = "DELETE FROM kasir WHERE id_kasir=?";

        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtid.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil dihapus");

            kosong();
            txtid.requestFocus();

        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal dihapus "+e);
        }

        datatable();
    }
    }//GEN-LAST:event_bhapusActionPerformed

