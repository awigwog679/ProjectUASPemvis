protected void datatable(){
    Object[] Baris = {"ID Kasir","Nama Kasir","Jenis Kelamin","Agama","Alamat","No. Telepon","Password"};
    tabmode = new DefaultTableModel(null, Baris);
        tblkasir.setModel(tabmode); 
        
        String sql = "SELECT * FROM kasir";
        
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String a = hasil.getString("id_kasir");
                String b = hasil.getString("nama_kasir"); 
                String c = hasil.getString("jenis_kelamin");
                String d = hasil.getString("no_telp");
                String e = hasil.getString("agama");
                String f = hasil.getString("password");
                String g = hasil.getString("alamat");

                String[] data = {a, b, c, d, e, f, g};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil " + e);
        }
    }
